/// <summary>
/// Define las categorías generales de los ataques del juego.
/// </summary>
public enum AttackType
{
    /// <summary>
    /// Ataque cuerpo a cuerpo de resolución inmediata.
    /// </summary>
    Melee,

    /// <summary>
    /// Ataque a distancia resuelto mediante proyectiles.
    /// </summary>
    Ranged,

    /// <summary>
    /// Ataque de área.
    /// </summary>
    AreaOfEffect
}
